package Inheritance;

public class InherExample {
	//simple example:
	public class Animal{	//SUPER CLASS
	}	
	public class Dog extends Animal {	//Inherit from Animal	
	}
	public class Cat extends Dog{//Inherit from Animal too
	}
	
	//Types of Inheritance:
	//Class->Class || Interface->Interface  USING 'extends'
	//Class->Interface USING 'implements'
}
